const os = require('os')

// platform
console.log(os.platform())

// architecture
console.log(os.arch())

// cpu
console.log(os.cpus())

// free ram memory
console.log(os.freemem())

// all ram memory
console.log(os.totalmem())

// home directory
console.log(os.homedir())

// device work time
console.log(os.uptime())
